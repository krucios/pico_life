//
// Better than rand() as it gives rand sequences out of reset
// when rand() always generates same sequence.
//
uint32_t rnd(void){
  int random = 0;
  volatile uint32_t *rnd_reg=(uint32_t *)(ROSC_BASE + ROSC_RANDOMBIT_OFFSET);

  for (int k = 0; k < 32; k++) {
    random = random << 1;
    random = random + (0x00000001 & (*rnd_reg));
  }

  return random;
}


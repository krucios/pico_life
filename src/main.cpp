//
// Clears game's state. Useful if need to start from blank
//
void clear_state() {
  for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
    for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
      state[y][x] = 0;
    }
  }
}

//
// Assigns random values to a state so life begins
//
void randomize_state() {
  for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
    for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
      state[y][x] = rand() % 2;
    }
  }
}

//
// Displays state on Cosmic screen. Important to clear
// graphics with RGB(0, 0, 0) before drawing
//
void draw_state() {
  graphics.set_pen(0, 0, 0);
  graphics.clear();

  for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
    for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
      if (state[y][x]) {
        graphics.set_pen(colors[color_idx].to_rgb888());
        graphics.pixel(Point(x, y));
      }
    }
  }

  cosmic_unicorn.update(&graphics);
}

//
// Inserts a glider at a given coordinates. Doesn't
// touch rest of state. Parameters:
// dx, dy - coordinates of left top corner from which
//          glider will be inserted
// invert - tells if the glider shall be inverted
//
void insert_glider(int dx, int dy, bool invert) {
  if (invert) {
    state[dy    ][dx    ] = true;
    state[dy    ][dx + 1] = true;
    state[dy    ][dx + 2] = true;
    state[dy + 1][dx + 2] = true;
    state[dy + 2][dx + 1] = true;
  } else {
    state[dy    ][dx + 1] = true;
    state[dy + 1][dx + 2] = true;
    state[dy + 2][dx    ] = true;
    state[dy + 2][dx + 1] = true;
    state[dy + 2][dx + 2] = true;
  }
}

//
// Inserts a selected pattern at a given coordinates.
// Make sure pattern_n is in range of available patterns!
// This function doesn't clear rest of state. Parameters:
// dx, dy - coordinates of left top corner from which
//          pattern will be inserted
// pattern_n - pattern number to be inserted
//
void insert_pattern(int dx, int dy, int pattern_n) {
  for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
    for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
      state[y + dy][x + dx] |= patterns[pattern_n][y][x];
    }
  }
}

//
// Counts amount of alive neighbours. Borders are linked so
// the surface has toroid topology. Parameters:
// x, y - coordinates of cell for which calculation is requested
// returns (int) amount of alive neighbours
//
int count_alive_neighbours(int x, int y) {
  int count = 0;

  for (int i = 0; i < dsize; i++) {
    int nx = (x + dx[i] + CosmicUnicorn::WIDTH ) % CosmicUnicorn::WIDTH;
    int ny = (y + dy[i] + CosmicUnicorn::HEIGHT) % CosmicUnicorn::HEIGHT;
    count += state[ny][nx];
  }

  return count;
}

//
// A method to scroll a given message few times. Parameters:
// msg - message to display
// times - how many times to scroll the message
//
void show_message(const std::string& msg) {
  float scroll = -32.0f;

  while (scroll < 5) {
    scroll += 0.10f;

    graphics.set_pen(0, 0, 0);
    graphics.clear();

    graphics.set_pen(colors[color_idx].to_rgb888());
    graphics.text(msg, Point(0, scroll), -1, 0.55);

    cosmic_unicorn.update(&graphics);

    sleep_ms(2);
  }
}


int main() {
  bool skip_update = false; // Used to hold simulation while button is pressed

  stdio_init_all();
  cosmic_unicorn.init();
q
  randomize_state();

  while (true) {
    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_BRIGHTNESS_UP)) {
      if (!brightness_idx_lock) {
        brightness_idx_lock = true;
        brightness_idx = (brightness_idx + 1) % (sizeof(brightness) / sizeof(*brightness));
        cosmic_unicorn.set_brightness(brightness[brightness_idx]);
      }
    } else {
      brightness_idx_lock = false;
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_BRIGHTNESS_DOWN)) {
      if (!color_idx_lock) {
        color_idx_lock = true;
        color_idx = (color_idx + 1) % (sizeof(colors) / sizeof(*colors));
      }
    } else {
      color_idx_lock = false;
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_VOLUME_UP)) {
      delay_ms = MIN(1000, delay_ms + 10);
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_VOLUME_DOWN)) {
      delay_ms = MAX(20, delay_ms - 10);
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_SLEEP)) {
      clear_state();
      skip_update = true;
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_A)) {
      insert_glider(0, 0, false);
      skip_update = true;
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_B)) {
      insert_glider(0, CosmicUnicorn::HEIGHT - 3, true);
      skip_update = true;
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_C)) {
      randomize_state();
    }

    if (cosmic_unicorn.is_pressed(cosmic_unicorn.SWITCH_D)) {
      //
      // Randomize pattern idx only first time and then keep it
      // untill button is released as otherwise patterns will be
      // interleaved
      //
      if (!pattern_idx_lock) {
        pattern_idx = rand() % 3;
        pattern_idx_lock = true;
      }

      insert_pattern(0, 0, pattern_idx);
      skip_update = true;
    } else {
      pattern_idx_lock = false;
    }

    draw_state();

    if (skip_update) {
      skip_update = false;
      continue;
    }

    for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
      for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
        int alive_neighbours = count_alive_neighbours(x, y);

        if (state[y][x]) {
          next[y][x] = alive_neighbours >= 2 && alive_neighbours <= 3;
        } else {
          next[y][x] = alive_neighbours == 3;
        }
      }
    }

    bool same_states = true;
    bool is_empty = true;

    for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
      for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
        is_empty &= !prev[y][x];
        if (prev[y][x] != next[y][x]) {
          same_states = false;
          break;
        }
      }
      if (!same_states) {
        break;
      }
    }

    if (same_states && !is_empty) {
      curr_same_state_count++;
    } else {
      curr_same_state_count = 0;
    }

    for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
      for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
        prev[y][x] = state[y][x];
      }
    }

    for (int y = 0; y < CosmicUnicorn::HEIGHT; y++) {
      for (int x = 0; x < CosmicUnicorn::WIDTH; x++) {
        state[y][x] = next[y][x];
      }
    }

    if (curr_same_state_count > same_state_limit) {
      curr_same_state_count = 0;
      show_message(message);
      sleep_ms(2000);
      randomize_state();
    }

    sleep_ms(delay_ms);
  }

  return 0;
}
